        <h1><center>Butler Polymer Research Lectureship Series</center></h1>
        <!-- <img class="imgr borderedbox" src="images/demo/120x120.gif" alt=""> -->
        
        <p>The Butler Lectureship Series in polymer chemistry and 
                engineering was established in 1998 by an endowment provided by 
                George and Josephine Butler with the intent of bringing the world’s 
                best authorities in macromolecules to our campus. The series has 
                been patterned after the Baker Lectureship Series at Cornell 
                University, whereby speakers are invited to spend one month or 
                longer within the department presenting a series of 10 or more 
                lectures in addition to interacting with graduate students and 
                faculty as well. </p>
              <p>To date, seven lecturers have spent time with us at the 
                University of Florida in this capacity: </p>
              <p><a href="http://www.mpip-mainz.mpg.de/groups/wegner" target="_blank">Professor 
                Gerhard Wegner</a><br>
                Max Planck Institute for Polymer Research, Mainz, Germany<br>
                “Supramolecular Chemistry”<br>
                Spring 1999 </p>
              <p><a href="http://www.kaust.edu.sa/office-vp-research-jean-frechet.html" target="_blank">Professor Jean 
                Frechet</a><br>
                University of California, Berkeley<br>
                “Functional Polymers: From Design to Applications”<br>
                Fall 2000 </p>
              <p><a href="https://tirrell-lab.caltech.edu/DavidTirrell" target="_blank"> Professor David Tirrell</a><br>
                California Institute of Technology<br>
                “A Polymer Chemist’s View of Protein Synthesis &amp; Design”<br>
                Fall 2002 </p>
              <p><a href="https://msne.rice.edu/nedthomas/" target="_blank">Professor 
                Edwin “Ned” Thomas</a><br>
                Massachusetts Institute of Technology<br>
                “The Science &amp; Engineering of Block Polymer Based Materials”<br>
                Fall 2003 </p>
              <p><a href="http://www.mpip-mainz.mpg.de/groups/muellen" target="_blank"> Professor Klaus Müllen </a><br>
