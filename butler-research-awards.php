        <h1><center>Butler Polymer Research Award</center></h1>
        <!-- <img class="imgr borderedbox" src="images/demo/120x120.gif" alt=""> -->
        
        <p>Professor George & Josephine Butler established this award to recognize excellence in graduate student research and education related to polymer chemistry. Competition for the award is quite strong and extends beyond The Butler Polymer Research Laboratory. Candidates are selected from all Department of Chemistry PhD students carrying out their research in polymer chemistry.</p>
        
        <p>The 2015 award was presented to <a href="#">Megan Hill</a>. Hill has been an involved member of the UF Polymer Program and continues to make her mark with research publications in the Sumerlin Research Group. To read more about Megan, click <a href="http://www.cmse.ufl.edu/people/grad-students/megan-hill.php" target="_blank">here</a>.</p>
        
        
