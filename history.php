      <h1><center>The History of The George and Josephine Butler Polymer Research Laboratory</center></h1>
      <img class="imgr borderedbox" src="images/history2.jpg" alt="Dr. Ken Wagener">
      <p>Research in polymer chemistry and engineering started in 1946 
                when <a href="http://www.cmse.ufl.edu/people/alumni/george-butler.php" target="_blank">Professor George Butler</a> moved to Gainesville to begin a 
                teaching career here. As a consequence, this laboratory, which is 
                more than 60 years old now, is amongst the oldest in the United 
                States to conduct research in polymer science within a department of 
                chemistry. It started as a relatively small group of researchers and 
                remained that way until 1970 when a physical polymer chemist, Thieo 
                Hogen Esch, was brought to the Department. 
                Ken Dill joined in the early 1980’s, later moving to the 
                University of California/San Francisco where he is today. 
                </p>
      <img class="imgl borderedbox" src="images/history1.jpg" alt="George and Josephine Butler">
      <p>This is not to say that research in polymers was only happening 
                within the Butler and Hogen Esch research laboratories, for that is not the case. 
                Many others were involved to greater or lesser extents, including 
                Charles Reed in the early years. Professor Reed was among 
                the first in the world to investigate the use of polymer membranes 
                for reverse osmosis purification of salt water to create pure 
                drinking water. <br>
                <br>
                Nevertheless, the origin of what is known today as the George and 
                Josephine Butler Polymer Research Laboratory undoubtedly can be 
                attributed to the work of Butler and Hogen-Esch. In 1984 a decision 
                was made to expand further by the addition of Ken Wagener to the 
                faculty; Randy Duran came in 1989, John Reynolds in 1992. <br>
                <br>
                The George &amp; Josephine Butler Research Laboratory became formally 
                recognized as a separate entity in 1995 when it was dedicated as 
                part of the renovation of Leigh Hall, one of the Department’s oldest Buildings. 
                The Butler Laboratory is found on the third floor of this building, and 
                at present is occupied by the <a href="http://www.chem.ufl.edu/~castellano/" target="_blank">Castellano</a>, <a href="https://colina.chem.ufl.edu/" target="_blank">Colina</a>,
<a href="http://www.chem.ufl.edu/research/facultypage.shtml?photo=fanucci/" target="_blank">Fanucci</a>, 
<a href="http://web.chem.ufl.edu/groups/miller/" target="_blank">Miller</a>,
<a href="https://savin.chem.ufl.edu/" target="_blank">Savin</a>, 
<a href="http://www.chem.ufl.edu/~kschanze/" target="_blank">Schanze</a>, 
<a href="http://www.chem.ufl.edu/~sumerlin/" target="_blank">Sumerlin</a>,
<a href="https://veige.chem.ufl.edu/" target="_blank">Veige</a>,
 and 
<a href="http://www.chem.ufl.edu/~wagener/" target="_blank">Wagener</a> research groups</p>
