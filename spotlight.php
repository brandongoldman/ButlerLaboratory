      <h6>About Dr. Savin</h6>
      <div class="imgl borderedbox"><img src="images/spotlight/savin.jpg" alt="Dr. Daniel Savin"></div>
      <p>Dr. Daniel Savin graduated from <a href="https://www.hmc.edu/" target="_blank">Harvey Mudd College</a> (Claremont, CA) in 1995 with  a B.S. in Chemistry and received an M.S. in Polymer Science and a Ph.D. in  Chemistry in 1997 and 2002, respectively, at <a href="http://www.cmu.edu/index.shtml" target="_blank">Carnegie Mellon University</a> under  the direction of Prof. Gary Patterson.&nbsp;  His early research involved the study of dynamics in glassy  materials.&nbsp; He continued this work as a  Postdoctoral Fellow in the group of Prof. Timothy Lodge at the University of  Minnesota from 2002-2003.&nbsp; In 2003 he  joined the <a href="http://www.uvm.edu/~chem/" target="_blank">Department of Chemistry at the University of Vermont</a> as an Assistant  Professor and in 2008 he moved to the <a href="http://www.usm.edu/polymer" target="_blank">School of Polymers at Southern Miss</a>.&nbsp; His current research involves the synthesis  and characterization of stimuli-responsive peptide block copolymers for use in  controlled release applications.&nbsp; He has  additional projects studying impact energy-absorbing materials for use in  personal protective equipment, and in developing next-generation oil  dispersants.</p>
       
