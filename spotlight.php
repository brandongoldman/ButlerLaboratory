      <p>Research in the Savin group is largely associated with the applications of self-assembled polymers and block copolymers, with particular emphasis on solution characterization and property-structure relationships. My research projects can be broken into three broad categories: (1) Morphology transitions in polypeptide-based block copolymers with complex topologies, (2) Nanoparticle interactions and templates for novel remediation and delivery agents, and (3) Impact energy-absorbing polymer nanocomposites.  Our principal analytical tools are dynamic light scattering and rheology, although we invoke most polymer solution characterization techniques. We also rely heavily on microscopy (TEM and AFM) and mechanical testing (DMA, Instron, Dynatup).  All polymers are made in house, so we have standard synthetic tools including a number of shlenk lines and vacuum ovens. The PI has extensive experience in polymer characterization, in particular light scattering techniques for both solution and bulk/gel systems. The below video is Dr. Savin's presentation at Daytona State College on the topic of "Responsive block polymers using polypeptides: Bottom-up design in macromolecular materials."</p><br>
      
